package data;

public class Actor {

    public String firstName;
    public String lastName;
    public int dayShooting;
    public boolean promoTeam;
    public String role;

    public Actor(String f, String l, int s, boolean p, String r) {
        firstName = f;
        lastName = l;
        dayShooting = s;;
        promoTeam = p;
        role = r;
    }

}
